# math module
# This module provides access to the mathematical functions defined by the C standard.
# It is a built-in modedule in python and does not need to be installed
# It provides access to the mathematical functions defined by the C standard

import math

print(math.pi)  # 3.1415
# 6 # returns the smallest integer greater than or equal to x))
print(math.ceil(5.5))
print(math.floor(5.5))  # 5 # returns the largest integer less than or equal to x
print(math.factorial(5))  # 120 # returns the factorial of x
print(math.gcd(5, 10))  # 5 # returns the greatest common divisor of x and y
print(math.lcm(5, 10))  # 10 # returns the least common multiple of x and y
print(math.sqrt(5))  # 2.23606797749979 # returns the square root of x
print(math.pow(5, 2)) # 25.0 # returns the power of x to y
print(math.exp(5)) # 148.4131591025766 # returns the value of e raised to the power of x
print(math.log(5)) # 1.6094379124341003 # returns the natural logarithm of x
print(math.log10(5)) # 0.698970004336019 # returns the base 10 logarithm of x
print(math.log2(5)) # 2.321928094887362 # returns the base 2 logarithm of x
print(math.sin(5)) # -0950588235294117645 # returns the sine of x
print(math.cos(5)) # 0.28366218546322625 # returns the cosine of x 

